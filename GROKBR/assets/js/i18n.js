var _lpSelected = 'hybrid';

function lpSelect(btn){
  document.querySelectorAll('.lp-btn').forEach(function(b){
    b.classList.remove('active');
    b.querySelector('.lp-btn-check').innerHTML='';
  });
  btn.classList.add('active');
  btn.querySelector('.lp-btn-check').innerHTML='✓';
  _lpSelected = btn.getAttribute('data-lang');
}

function lpApply(lang){
  var t = LP_TRANSLATIONS[lang];
  if(!t) return;
  document.querySelectorAll('[data-i18n]').forEach(function(el){
    var key = el.getAttribute('data-i18n');
    if(t[key] !== undefined) el.innerHTML = t[key];
  });
  document.documentElement.setAttribute('data-lang', lang);
}

var _fabLabels = {hybrid:'Hybrid', pt:'PT 🇧🇷', en:'EN 🇺🇸'};

function lpOpen(){
  // garante sincronia com o que foi salvo
  var saved = localStorage.getItem('grokbr-lang');
  if(saved && LP_TRANSLATIONS[saved]) _lpSelected = saved;

  var ov = document.getElementById('lpOverlay');
  document.querySelectorAll('.lp-btn').forEach(function(b){
    var isActive = b.getAttribute('data-lang') === _lpSelected;
    b.classList.toggle('active', isActive);
    b.querySelector('.lp-btn-check').innerHTML = isActive ? '✓' : '';
  });
  ov.style.display='flex';
  ov.style.animation='lp-fade .3s ease';
}

function lpConfirm(){
  localStorage.setItem('grokbr-lang', _lpSelected);
  lpApply(_lpSelected);
  // atualiza label do FAB
  var tag = document.getElementById('fabLangTag');
  if(tag) tag.textContent = _fabLabels[_lpSelected] || _lpSelected;
  var ov = document.getElementById('lpOverlay');
  ov.style.animation='lp-fade .25s ease reverse forwards';
  setTimeout(function(){ ov.style.display='none'; }, 240);
}

(function(){
  var saved = localStorage.getItem('grokbr-lang');
  if(saved && LP_TRANSLATIONS[saved]){
    _lpSelected = saved;
    lpApply(saved);
    var tag = document.getElementById('fabLangTag');
    if(tag) tag.textContent = _fabLabels[saved] || saved;
  } else {
    setTimeout(function(){
      document.getElementById('lpOverlay').style.display='flex';
    }, 800);
  }
})();